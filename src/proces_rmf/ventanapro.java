    public void rellemarlista(){
    DefaultListModel model=new DefaultListModel();
    
    try {
       Process P = Runtime.getRuntime().exec("cmd /c TASKLIST");
        InputStreamReader input = new InputStreamReader(P.getInputStream());
        BufferedReader g=new BufferedReader(input);
        String f=g.readLine();
        int w=0;
        while(f!=null){
      
        model.addElement(f.split(" ")[0].trim());
        C1.procesos.add(f);
        f=g.readLine();
        w=w+1;
          
        }
        proces.setModel(model);
    } catch (IOException ex) {
        Logger.getLogger(ventanapro.class.getName()).log(Level.SEVERE, null, ex);
    }
   
    }
